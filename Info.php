    <h2 class="titlu">Crame moldovenești</h2>
    
    <p class="informatie">Multe crame moldovenești în perioada sovietică erau denumite combinate și fabrici de vin. După destrămarea blocului comunist situația lor a devenit cristică, unele crame au rezistat crizei și s-au reorganizat, altele au dispărut pentru totdeauna.
    Pe lângă marii producători cu un trecut în spate precum Château Purcari, Cricova, Mileștii-Mici, Bostavan, Vinuri Comrat, Combinatul din Tomai, Pivnițele de la Brănești, Kvint, Călărași Divin.
    În perioada postcomunistă s-au creat „cramele de intersecții”: Vinăria din Vale, Château Migdal-P, Château Vartely, Vinăria Fautor, Vinăria Cimișlia, Dionysos-Mereni, Domeniile Căinari, Vinăria Sălcuța, Vinăria Gitana. Alte vinării medii care s-u impus prin calitate sunt Timbrus Purcari Estate, Vinăria Asconi și Castel Mimi (poartă numele celui mai mare și vechi conac de peste Prut, construit în perioada interbelică, astăzi fiind considerat un patrimoniu national)
    Vinăriile mici sau de “garaj”- În această categorie sunt incluse toate vinăriile mici ce fac parte din Asociația Micilor Producători de Vin din Moldova: Crama Domnească (vinurile Equinox), Vinăria Gogu, Vinăria EtCetera, Vinăria Nobilă, Vinăria Dac, Vinăria Atuu, Vinăria Agrici, Vinăria Poiana, Vinăria Unicorn, Vinăria Mezalimpe, Minis Terrios, Vinăria Corten (cândva Vinăria Molda), Pelicanul Negru, Vinăria Gara-Gani. Majoritatea micilor productărori de vin care fac parte din asociație trebuie să dețină până în 30 ha de viță-de-vie.
