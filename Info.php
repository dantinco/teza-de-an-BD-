    <h2 class="titlu">Industria vinului</h2>
    
    <p class="informatie">Datele statistice oferite de Biroul Național de Statistică al Republicii Moldova pe anul 2014 confirmă că volumul exporturilor vinurilor moldovenești a crescut cu 6% odată cu decizia UE de a deschide complet piața pentru vinul moldovenesc. De asemenea, un rol important la jucat și brandul Wine of Moldova implementat de autorități cu scopul de a promova vinul moldovenesc în lume. Exportul vitivinicol este foarte important pentru mică republica deoarece asigura 10-15% din bugetul total, iar sectorul vinicol formează 3,2% din produsul intern brut și 5% din totalul exporturilor din R. Moldova.
    Mai mult decât atât, piața din Federația Rusă nu mai este atractivă pentru mulți producători de vin din Moldova, cu excepția unor producători din UTA Găgăuzia, în timp ce micii producători s-au orientat către piețe precum China, Coreea de Sud, SUA și Europa. România este o piață de desfacere importantă pentru unele produse moldovenești, mai ales pentru vinuri. Odată cu embargoul rusesc din 2006, 2010 și septembrie 2013 (vezi și Embargoul rus asupra vinurilor din Republica Moldova (2013-prezent)) România a fost un colac de salvare pentru mulți producători de vinuri. Doar 13% din vinul important în România este din Republica Moldova. Tot din datele Biroului Național de Statistică din Moldova reiese că o cantitate însemnată de vinuri pleacă spre piețe din China (0,32%,), Polonia (cota de 3,1%) și SUA care pledează pentru vinuri premium(0,06%.)
    Conform unui raport al Băncii Mondiale din decembrie 2015 exportul de vinuri a scăzut cu aproximativ 81 milioane dolari în perioada 2003-2013. Acest lucru s-a datorat politicilor în ceea ce privește exportul către anumite regiuni (cum ar fi Rusia) plus schimbarea zonei de export către Europa, acest lucru generând nevoia de a mări calitatea produselor exportate.
    </p>
