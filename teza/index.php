    <h2 class="titlu">Vinul in Moldova</h2>
    
    <p class="comment">De-a lungul istoriei sale, vinul și strugurii au fost strâns împletiți în cultura, miturile, folclorul și legendele țării fiind parte indispensabilă a tradițiilor, obiceiurilor și gastronomiei locale.
        Industria vinului din Moldova este bine dezvoltată. Suprafața ocupată de podgorii este de aproximativ 147.000 de hectare, din care 102.500 de hectare sunt folosite în scop comercial. Aproximativ 85% din volumul de vin produs este comercializat în afara țării noastre în 63 de țări ale lumii, plasând vinul printre cele mai importante produse de export ale economiei noastre. În prezent, peste 70% din vinurile îmbuteliate sunt exportate în țările UE, Asia Pacific și America de Nord.
        Multe familii au propriile rețete și soiuri de struguri care s-au transmis din generație în generație. 
